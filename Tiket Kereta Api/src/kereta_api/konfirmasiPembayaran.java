    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        String ID = id.getInstance().getID();
        try {
            try (Connection conn = koneksi.getConnection()) {
                Statement stmt = conn.createStatement();
                String query = "UPDATE tbl_pesanan SET status_pembayaran = 'Sudah Dibayar' WHERE id_pesanan = '" + ID + "'";
                int result = stmt.executeUpdate(query);
                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Status pembayaran berhasil diperbarui.");
                } else {
                    JOptionPane.showMessageDialog(this, "Pesanan dengan ID " + ID + " tidak ditemukan.");
                }
            }
        } catch (HeadlessException | SQLException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Failed to update payment status.");
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

