    private void konfirmasi(){
        String ID = id.getInstance().getID();
        try {
            try (Connection conn = koneksi.getConnection()) {
                Statement stmt = conn.createStatement();
                String query = "SELECT p.*, k.nama_kereta FROM tbl_pesanan p JOIN tbl_kereta k ON p.asal = k.asal_kereta AND p.tujuan = k.tujuan_kereta WHERE p.id_pesanan ='" + ID + "'";
                ResultSet rs = stmt.executeQuery(query);
                if (rs.next()) {
                    String nama = rs.getString("nama");
                    String email = rs.getString("email");
                    String asal = rs.getString("asal");
                    String tujuan = rs.getString("tujuan");
                    String jumlah = rs.getString("jumlah_tiket");
                    String harga = rs.getString("harga");
                    String total = rs.getString("total_harga");
                    String kereta = rs.getString("nama_kereta");

                    lblNama.setText(nama);
                    lblEmail.setText(email);
                    lblAsal.setText(asal);
                    lblTujuan.setText(tujuan);
                    lblJumlah.setText(jumlah);
                    lblHarga.setText(harga);
                    lblTotal.setText(total);
                    lblKereta.setText(kereta);
                } else {
                    JOptionPane.showMessageDialog(this, "Kereta dengan ID " + ID + " tidak ditemukan.");
                }
            }
        } catch (HeadlessException | SQLException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Failed to retrieve data.");
        }
    }
    
